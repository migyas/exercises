// 1. Criando a variável
let texto = "Olá, mundo!";

// 2. Convertendo para letras maiúsculas
let maiusculas = texto.toUpperCase();
console.log(maiusculas); // Saída: "OLÁ, MUNDO!"

// 3. Convertendo para letras minúsculas
let minusculas = texto.toLowerCase();
console.log(minusculas); // Saída: "olá, mundo!"

// 4. Contando o número de caracteres
let numeroCaracteres = texto.length;
console.log(numeroCaracteres); // Saída: 12

// 5. Substituindo palavras
let substituido = texto.replace("mundo", "JavaScript");
console.log(substituido); // Saída: "Olá, JavaScript!"
